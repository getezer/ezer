# ── Route Determination ─────────────────────────────────────────────────────────

def determine_rider_route(user_schedule: dict) -> tuple[str, str]:
    """
    Determine which output route to take based on rider status in Tier 2.

    Returns:
        (route, reason) where route is "A", "B", or "C"

    Route A: Protector Rider is ACTIVE → insurer deducted wrongly → grievance
    Route B: Rider INACTIVE + age ineligibility flag → gap awareness
    Route C: Rider INACTIVE + no age flag → renewal action
    """
    addons = user_schedule.get("addons_active",
             user_schedule.get("addons", {}))

    protector_active = addons.get("protector_rider", False)

    if protector_active:
        return (
            "A",
            "Protector Rider is ACTIVE. Insurer deducted rider-dependent "
            "items that should have been paid. Grievance mode."
        )

    # Check for age-based ineligibility flag
    age_flag = addons.get("rider_ineligible_due_to_age", {})
    protector_age_flag = age_flag.get("protector_rider", "")

    if protector_age_flag:
        return (
            "B",
            f"Protector Rider is INACTIVE. Age-based ineligibility flag present: "
            f"{protector_age_flag[:80]}. Gap awareness mode."
        )

    # Rider inactive, no age flag — rider is available but not purchased
    return (
        "C",
        "Protector Rider is INACTIVE. No age restriction flag. "
        "Rider is available on this product. Renewal action mode."
    )


# ── Document 1: Hospital Irregularity Memo ──────────────────────────────────────

def generate_hospital_memo(memo_data: dict, claim_header: dict) -> str:
    """
    Generate the Hospital Irregularity Memo.
    Always generated — independent of rider status.
    Uses plain_english and evidence_required directly from audit output.
    Includes claim reference and hospital name. No patient PII.
    """
    W = 72
    lines = []

    lines.append("=" * W)
    lines.append("  EZER HOSPITAL IRREGULARITY MEMO")
    lines.append("  Document 1 of 3 — For Billing Desk Use")
    lines.append("=" * W)
    lines.append("")
    lines.append(f"  Claim Reference : {memo_data['claim_reference']}")
    lines.append(f"  Hospital        : {claim_header['hospital']}")
    lines.append(f"  Procedure       : {claim_header['procedure']}")
    lines.append(f"  Admission       : {claim_header['admission_date']} "
                 f"to {claim_header['discharge_date']}")
    lines.append(f"  Generated by    : Ezer Audit Engine V3.2")
    lines.append(f"  Generated on    : {datetime.today().strftime('%d/%m/%Y')}")
    lines.append("")
    lines.append("─" * W)
    lines.append("")
    lines.append("  This memo identifies billing irregularities detected by the Ezer")
    lines.append("  Audit Engine. We are requesting written clarification on the")
    lines.append("  following items. This is not a legal notice — it is a factual")
    lines.append("  audit request submitted in good faith before any escalation.")
    lines.append("")
    lines.append(f"  Total Amount Under Review : ₹{memo_data['total_amount']:,.2f}")
    lines.append(f"  Number of Flags           : {memo_data['total_flags']}")
    lines.append("")
    lines.append("─" * W)

    for flag in memo_data["flags"]:
        lines.append("")
        lines.append(f"  FLAG {flag['flag_number']} — {flag['flag_id']} "
                     f"[{flag['flag_type']}]")
        lines.append(f"  Item   : {flag['item']}")
        lines.append(f"  Amount : ₹{flag['amount']:,.2f}")
        lines.append("")

        # Plain English — word wrap at 68 chars
        plain = flag["plain_english"]
        lines.append("  What This Means:")
        words = plain.split()
        line = "    "
        for word in words:
            if len(line) + len(word) + 1 > 70:
                lines.append(line)
                line = "    " + word + " "
            else:
                line += word + " "
        if line.strip():
            lines.append(line)

        lines.append("")
        evidence = flag["evidence_required"]
        lines.append("  Evidence Requested:")
        words = evidence.split()
        line = "    "
        for word in words:
            if len(line) + len(word) + 1 > 70:
                lines.append(line)
                line = "    " + word + " "
            else:
                line += word + " "
        if line.strip():
            lines.append(line)

        lines.append("")
        lines.append("  " + "─" * (W - 2))

    lines.append("")
    lines.append("  We request written responses to each item within 7 working days.")
    lines.append("  If no response is received, we reserve the right to escalate")
    lines.append("  to the Insurance Ombudsman, Bhubaneswar.")
    lines.append("")
    lines.append("=" * W)

    return "\n".join(lines)


# ── Document 2: Settlement Comparison ──────────────────────────────────────────

def generate_settlement_comparison(audit_data: dict) -> str:
    """
    Generate the Settlement Comparison table.
    Always generated — independent of rider status.
    Two columns: Actual vs Ezer View. Numbers only. No opinion.
    """
    W = 72
    lines = []

    integrity  = audit_data["integrity"]
    asymmetry  = audit_data["asymmetry"]
    simulation = audit_data["simulation"]

    actual_grand    = integrity["grand_total"]
    total_deducted  = integrity["total_deducted"]
    layer1_invisible = asymmetry["layer_1_invisible_loss"]["total"]
    layer2_visible   = asymmetry["layer_2_visible_loss"]["total"]
    total_asymmetry  = asymmetry["total_asymmetry_payload"]
    recoverable      = simulation["recoverable_amount"]
    revised_total    = simulation["revised_grand_total"]

    lines.append("=" * W)
    lines.append("  EZER SETTLEMENT COMPARISON")
    lines.append("  Document 2 of 3 — Actual vs Ezer View")
    lines.append("=" * W)
    lines.append("")
    lines.append(f"  {'Item':<40} {'Actual':>12}  {'Ezer View':>12}")
    lines.append("  " + "─" * 68)

    lines.append(f"  {'Total Claimed by Hospital':<40} "
                 f"₹{integrity['total_claimed']:>10,.2f}  "
                 f"₹{integrity['total_claimed']:>10,.2f}")

    lines.append(f"  {'MOU Discount Applied':<40} "
                 f"₹{integrity['total_discount']:>10,.2f}  "
                 f"₹{integrity['total_discount']:>10,.2f}")

    lines.append(f"  {'Insurer Deductions':<40} "
                 f"₹{total_deducted:>10,.2f}  "
                 f"₹{2330.00:>10,.2f}")

    lines.append(f"  {'  — Rider Dependent (recoverable)':<40} "
                 f"₹{recoverable:>10,.2f}  "
                 f"₹{0.00:>10,.2f}")

    lines.append(f"  {'  — Non-Payable (correct)':<40} "
                 f"₹{2330.00:>10,.2f}  "
                 f"₹{2330.00:>10,.2f}")

    lines.append(f"  {'Hospital Flags — Paid Without Audit':<40} "
                 f"{'Not surfaced':>12}  "
                 f"₹{layer1_invisible:>10,.2f}")

    lines.append("  " + "─" * 68)

    lines.append(f"  {'Amount Settled (Grand Total)':<40} "
                 f"₹{actual_grand:>10,.2f}  "
                 f"₹{revised_total:>10,.2f}")

    lines.append("")
    lines.append("  " + "─" * 68)
    lines.append(f"  {'TOTAL ASYMMETRY PAYLOAD':<40} "
                 f"{'':>12}  "
                 f"₹{total_asymmetry:>10,.2f}")
    lines.append("")
    lines.append("  Asymmetry Payload = Insurer Deductions + Hospital Flags")
    lines.append(f"  = ₹{layer2_visible:,.2f} + ₹{layer1_invisible:,.2f} "
                 f"= ₹{total_asymmetry:,.2f}")
    lines.append("")

    # GST warning if present
    if audit_data["integrity"].get("warnings"):
        for w in audit_data["integrity"]["warnings"]:
            lines.append(f"  ⚠  {w}")
        lines.append("")

    lines.append("=" * W)

    return "\n".join(lines)


# ── Document 3: Rider Output (Route-Dependent) ──────────────────────────────────

def generate_rider_output(
    route: str,
    audit_data: dict,
    claim_header: dict
) -> str:
    """
    Generate the rider-dependent output document.
    Route A → Grievance letter
    Route B → Gap awareness + planning note
    Route C → Renewal action checklist
    """
    if route == "A":
        return _generate_grievance_letter(audit_data, claim_header)
    elif route == "B":
        return _generate_gap_awareness(audit_data, claim_header)
    else:
        return _generate_renewal_action(audit_data, claim_header)


