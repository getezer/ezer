# ── Full Report Orchestrator ────────────────────────────────────────────────────

def generate_full_report(
    settlement_path: Path = SETTLEMENT_FILE,
    user_schedule_path: Path = USER_SCHEDULE_FILE
) -> dict:
    """
    Orchestrates the full Symmetry Report.
    1. Runs audit engine
    2. Loads user schedule
    3. Determines route
    4. Generates all three documents
    5. Saves to reports/GPS_{claim_id}/
    Returns dict with route taken and file paths.
    """

    # Step 1 — Run audit
    audit_data = run_audit(settlement_path)

    # Step 2 — Load user schedule
    raw_schedule = json.loads(user_schedule_path.read_text(encoding="utf-8"))

    # Step 3 — Load settlement for claim header (non-PII fields only)
    raw_settlement = json.loads(settlement_path.read_text(encoding="utf-8"))
    header = raw_settlement.get("claim_header", {})
    meta   = raw_settlement.get("_schema_meta", {})

    claim_header = {
        "claim_reference":  meta.get("claim_reference", "UNKNOWN"),
        "policy_number":    meta.get("policy_number", "UNKNOWN"),
        "settlement_date":  header.get("admission_date", "UNKNOWN"),
        "hospital":         header.get("hospital", "UNKNOWN"),
        "procedure":        header.get("procedure", "UNKNOWN"),
        "admission_date":   header.get("admission_date", "UNKNOWN"),
        "discharge_date":   header.get("discharge_date", "UNKNOWN"),
    }

    # Step 4 — Determine route
    route, route_reason = determine_rider_route(raw_schedule)

    # Step 5 — Generate documents
    doc1 = generate_hospital_memo(audit_data["memo"], claim_header)
    doc2 = generate_settlement_comparison(audit_data)
    doc3 = generate_rider_output(route, audit_data, claim_header)

    # Step 6 — Save to reports folder organised by claim ID
    claim_id     = meta.get("claim_reference", "UNKNOWN").replace("-", "_")
    report_dir   = REPORTS_DIR / f"GPS_{claim_id}"
    report_dir.mkdir(parents=True, exist_ok=True)

    path1 = report_dir / "hospital_memo.txt"
    path2 = report_dir / "settlement_comparison.txt"
    path3 = report_dir / "rider_output.txt"

    path1.write_text(doc1, encoding="utf-8")
    path2.write_text(doc2, encoding="utf-8")
    path3.write_text(doc3, encoding="utf-8")

    return {
        "route":        route,
        "route_reason": route_reason,
        "report_dir":   str(report_dir),
        "files": {
            "hospital_memo":         str(path1),
            "settlement_comparison": str(path2),
            "rider_output":          str(path3)
        },
        "audit_summary": {
            "total_asymmetry":  audit_data["asymmetry"]["total_asymmetry_payload"],
            "invisible_loss":   audit_data["asymmetry"]["layer_1_invisible_loss"]["total"],
            "visible_loss":     audit_data["asymmetry"]["layer_2_visible_loss"]["total"],
            "recoverable":      audit_data["simulation"]["recoverable_amount"]
        }
    }


